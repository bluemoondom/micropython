class QMI8658:
    def __init__(self, address=0x6B):
        self._address = address
        self._bus = i2c
        if self._read_byte(0x00) != 0x05:
            self._address = 0x6A
            if self._read_byte(0x00) != 0x05:
                raise RuntimeError("QMI8658 nenalezen na I2C")
        self._config()

    def _read_byte(self, reg):
        try:
            return self._bus.readfrom_mem(self._address, reg, 1)[0]
        except OSError:
            return 0

    def _read_block(self, reg, length):
        return self._bus.readfrom_mem(self._address, reg, length)

    def _write_byte(self, reg, val):
        self._bus.writeto_mem(self._address, reg, bytes([val]))

    def _config(self):
        self._write_byte(0x02, 0x60)
        self._write_byte(0x03, 0x23)
        self._write_byte(0x04, 0x53)
        self._write_byte(0x05, 0x00)
        self._write_byte(0x06, 0x11)
        self._write_byte(0x07, 0x00)
        self._write_byte(0x08, 0x03)

    def Read_Raw_XYZ(self):
        raw = self._read_block(0x35, 12)
        out = []
        for i in range(6):
            v = (raw[i*2 + 1] << 8) | raw[i*2]
            if v >= 0x8000:
                v -= 0x10000
            out.append(v)
        return out

    def Read_XYZ(self):
        raw = self.Read_Raw_XYZ()
        acc_lsb_div  = 1 << 12
        gyro_lsb_div = 64
        xyz = [0.0] * 6
        for i in range(3):
            xyz[i]     = raw[i]     / acc_lsb_div
            xyz[i + 3] = raw[i + 3] / gyro_lsb_div
        return xyz

class CST816S:
    def __init__(self, address=0x15):
        self._address = address
        self._bus = i2c
        self._rst = Pin(TP_RST, Pin.OUT)
        self._int = Pin(TP_INT, Pin.IN, Pin.PULL_UP)
        # reset čipu
        self._rst(0); time.sleep_ms(10)
        self._rst(1); time.sleep_ms(80)
        try:
            self._bus.writeto_mem(self._address, 0xFE, b'\x01')
        except OSError:
            pass

    def read(self):
        """Vrátí (touched, x, y). Při chybě (False, 0, 0)."""
        try:
            d = self._bus.readfrom_mem(self._address, 0x02, 5)
        except OSError:
            return (False, 0, 0)
        fingers = d[0] & 0x0F
        if fingers == 0:
            return (False, 0, 0)
        x = ((d[1] & 0x0F) << 8) | d[2]
        y = ((d[3] & 0x0F) << 8) | d[4]
        return (True, x, y)


